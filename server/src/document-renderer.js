const fs = require("fs");
const path = require("path");
const {
  AlignmentType,
  Document,
  HeadingLevel,
  ImageRun,
  LevelFormat,
  Packer,
  Paragraph,
  TextRun
} = require("docx");
const { evaluateResumeRenderQuality } = require("./resume-render-qa");
const { DEFAULT_RESUME_TEMPLATE, resolveResumeTemplate } = require("./resume-template-registry");

async function renderResumeDocx(resumeVersion, options = {}) {
  const outputDir = path.resolve(options.outputDir || path.join(__dirname, "..", "data", "generated_resumes"));
  fs.mkdirSync(outputDir, { recursive: true });
  const fileName = buildResumeFileName(resumeVersion);
  const filePath = path.join(outputDir, fileName);
  const fields = resumeVersion.resumeFields || {};
  const template = resolveResumeTemplate(options.templateName || options.template || resumeVersion.renderMetadata?.template);
  const renderMetadata = {
    ...(resumeVersion.renderMetadata || {}),
    ...options,
    template: template.key,
    templateLabel: template.label,
    templateSkill: template.skillName,
    templateOrder: template.order,
    showSummarySection: template.showSummarySection,
    showSkillsSection: template.showSkillsSection,
    maxPages: template.maxPages
  };
  const doc = new Document({
    creator: "Boss Find",
    title: `${fields.name || "Candidate"} - ${fields.targetRole || "Resume"}`,
    description: `Generated by Boss Find local ResumeAgent using ${template.key}.`,
    numbering: {
      config: [{
        reference: "resume-bullets",
        levels: [{
          level: 0,
          format: LevelFormat.BULLET,
          text: "•",
          alignment: AlignmentType.LEFT,
          style: {
            paragraph: {
              indent: { left: 360, hanging: 180 }
            }
          }
        }]
      }]
    },
    styles: {
      default: {
        document: {
          run: {
            font: "Microsoft YaHei",
            size: template.bodyFontSize
          },
          paragraph: {
            spacing: { after: template.paragraphSpacingAfter }
          }
        }
      },
      paragraphStyles: [
        {
          id: "CompactHeading",
          name: "Compact Heading",
          basedOn: "Heading2",
          next: "Normal",
          quickFormat: true,
          run: { bold: true, size: template.headingFontSize },
          paragraph: { spacing: { before: template.sectionSpacingBefore, after: template.sectionSpacingAfter } }
        }
      ]
    },
    sections: [
      {
        properties: {
          page: {
            size: {
              width: 11906,
              height: 16838
            },
            margin: template.margin
          }
        },
        children: buildResumeParagraphs(fields, renderMetadata, template)
      }
    ]
  });
  const buffer = await Packer.toBuffer(doc);
  fs.writeFileSync(filePath, buffer);
  const result = {
    ok: true,
    format: "docx",
    fileName,
    filePath,
    byteLength: buffer.length,
    estimatedPages: estimatePages(fields),
    photoPath: renderMetadata.photoPath || "",
    referenceDocxPath: renderMetadata.referenceDocxPath || "",
    template: template.key,
    templateLabel: template.label,
    templateSkill: template.skillName,
    templateOrder: template.order,
    showSummarySection: template.showSummarySection,
    showSkillsSection: template.showSkillsSection,
    renderedAt: new Date().toISOString()
  };
  result.renderQuality = await evaluateResumeRenderQuality({
    filePath,
    resumeFields: fields,
    renderMetadata,
    rendered: result
  });
  return result;
}

function buildResumeParagraphs(fields = {}, renderMetadata = {}, template = resolveResumeTemplate(DEFAULT_RESUME_TEMPLATE)) {
  const paragraphs = [];
  const photo = buildPhotoParagraph(renderMetadata.photoPath || fields.photoPath || "");
  if (photo) {
    paragraphs.push(photo);
  }
  paragraphs.push(new Paragraph({
    alignment: AlignmentType.CENTER,
    children: [new TextRun({ text: fields.name || "Candidate", bold: true, size: template.nameFontSize })]
  }));
  const headline = [fields.headline, fields.targetRole].filter(Boolean).join(" | ");
  if (headline) {
    paragraphs.push(new Paragraph({
      alignment: AlignmentType.CENTER,
      children: [new TextRun({ text: headline, size: 20 })]
    }));
  }
  const contactLine = buildContactLine(fields.contact || {}, fields);
  if (contactLine) {
    paragraphs.push(new Paragraph({
      alignment: AlignmentType.CENTER,
      children: [new TextRun({ text: contactLine, size: 18 })]
    }));
  }
  for (const section of template.order || []) {
    if (section === "summary" && template.showSummarySection) {
      addSection(paragraphs, "求职摘要", [fields.summary || ""]);
    } else if (section === "skills" && template.showSkillsSection) {
      addSection(paragraphs, "技能", [formatInlineList(fields.skills || [])]);
    } else if (section === "education") {
      addEducation(paragraphs, fields.education || []);
    } else if (section === "projects") {
      addProjectSections(paragraphs, fields.projects || [], template);
    } else if (section === "awards") {
      addSection(paragraphs, "奖项与证书", fields.awards || []);
    }
  }
  return paragraphs.filter(Boolean);
}

function addProjectSections(paragraphs, projects, template = {}) {
  if (!projects.length) {
    return;
  }
  paragraphs.push(heading("项目经历"));
  for (const project of projects) {
    paragraphs.push(new Paragraph({
      children: [
        new TextRun({ text: project.title || "项目经历", bold: true }),
        new TextRun({ text: [project.organization, project.role, project.period].filter(Boolean).join(" | ") ? ` | ${[project.organization, project.role, project.period].filter(Boolean).join(" | ")}` : "" })
      ]
    }));
    if (template.embedSkillsInProjects !== false && project.skills?.length) {
      paragraphs.push(new Paragraph(`技术/能力：${formatInlineList(project.skills)}`));
    }
    for (const bullet of project.bullets || []) {
      paragraphs.push(bulletParagraph(bullet));
    }
  }
}

function addEducation(paragraphs, education) {
  if (!education.length) {
    return;
  }
  paragraphs.push(heading("教育经历"));
  for (const item of education) {
    paragraphs.push(new Paragraph({
      children: [
        new TextRun({ text: item.title || item.organization || "教育经历", bold: true }),
        new TextRun({ text: [item.organization, item.role, item.period].filter(Boolean).join(" | ") ? ` | ${[item.organization, item.role, item.period].filter(Boolean).join(" | ")}` : "" })
      ]
    }));
    for (const bullet of item.bullets || []) {
      paragraphs.push(bulletParagraph(bullet));
    }
  }
}

function addSection(paragraphs, title, lines) {
  const items = Array.isArray(lines) ? lines.filter(Boolean) : [lines].filter(Boolean);
  if (!items.length) {
    return;
  }
  paragraphs.push(heading(title));
  for (const line of items) {
    paragraphs.push(new Paragraph(String(line)));
  }
}

function heading(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_2,
    style: "CompactHeading"
  });
}

function bulletParagraph(text) {
  return new Paragraph({
    children: [new TextRun(String(text || ""))],
    numbering: { reference: "resume-bullets", level: 0 }
  });
}

function formatInlineList(items) {
  return (Array.isArray(items) ? items : []).filter(Boolean).join("、");
}

function buildContactLine(contact = {}, fields = {}) {
  const candidates = [
    contact.phone || fields.phone,
    contact.email || fields.email,
    contact.website || fields.website,
    contact.github || fields.github
  ];
  return candidates.map((item) => String(item || "").trim()).filter(Boolean).join(" | ");
}

function buildPhotoParagraph(photoPath) {
  const resolved = photoPath ? path.resolve(photoPath) : "";
  if (!resolved || !fs.existsSync(resolved)) {
    return null;
  }
  const extension = path.extname(resolved).slice(1).toLowerCase();
  const type = extension === "jpg" ? "jpeg" : extension;
  if (!new Set(["png", "jpeg", "gif", "bmp"]).has(type)) {
    return null;
  }
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    children: [new ImageRun({
      type,
      data: fs.readFileSync(resolved),
      transformation: { width: 82, height: 108 },
      altText: {
        title: "Candidate photo",
        description: "Candidate resume photo",
        name: "candidate-photo"
      }
    })]
  });
}

function buildResumeFileName(resumeVersion) {
  const safeTitle = safeFilePart(resumeVersion.title || resumeVersion.jobTitle || "resume");
  return `application-${resumeVersion.applicationId || "unknown"}-resume-${resumeVersion.id || Date.now()}-${safeTitle}.docx`;
}

function safeFilePart(value) {
  return String(value || "")
    .replace(/[\\/:*?"<>|]+/g, "-")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .slice(0, 60)
    .replace(/^-|-$/g, "") || "resume";
}

function estimatePages(fields = {}) {
  const totalChars = [
    fields.name,
    fields.headline,
    fields.targetRole,
    fields.summary,
    ...(fields.skills || []),
    ...(fields.awards || []),
    ...(fields.projects || []).flatMap((project) => [
      project.title,
      project.organization,
      project.role,
      ...(project.skills || []),
      ...(project.bullets || [])
    ]),
    ...(fields.education || []).flatMap((item) => [
      item.title,
      item.organization,
      item.role,
      ...(item.bullets || [])
    ])
  ].join(" ").length;
  return Math.max(1, Math.ceil(totalChars / 1800));
}

module.exports = {
  renderResumeDocx
};
